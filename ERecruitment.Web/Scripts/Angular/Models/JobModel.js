function JobModel(JobID, JobName, UserID, MinimumExperiance, MaximumExperiance, SubmissionDeadline) {
    this.JobID = JobID;
    this.JobName = JobName;
    this.UserID = UserID;
    this.MinimumExperiance = MinimumExperiance;
    this.MaximumExperiance = MaximumExperiance;
    this.SubmissionDeadline = SubmissionDeadline;
}